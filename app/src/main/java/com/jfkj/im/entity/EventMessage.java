package com.jfkj.im.entity;

public class EventMessage {


    private int type;


    private String Message ;


    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getMessage() {
        return Message;
    }

    public void setMessage(String message) {
        Message = message;
    }
}
